if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="AI 威胁情报分析面板 — Gemma-4-E4B-it + Cybersecurity LoRA",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python3 threat-intel.py                              # 交互面板
  python3 threat-intel.py --ioc "185.220.101.34"       # 快速 IOC 分析
  python3 threat-intel.py --cve "CVE-2024-3094"        # CVE 分析
  python3 threat-intel.py -f apt29.txt -o report.md    # 文件分析 + 导出
        """,
    )
    parser.add_argument("--model", default="./models/google/gemma-4-E4B-it",
                        help="Base model directory")
    parser.add_argument("--lora", default="./gemma4-it-cybersec-lora-ms-single-gpu",
                        help="LoRA adapter directory")
    parser.add_argument("--ioc", help="Quick IOC analysis")
    parser.add_argument("--cve", help="Quick CVE analysis")
    parser.add_argument("--file", "-f", help="Read intelligence from file")
    parser.add_argument("--output", "-o", help="Output Markdown report path")
    args = parser.parse_args()

    engine = ThreatIntelEngine(args.model, args.lora)

    if args.ioc:
        report = engine.analyze_ioc(args.ioc)
        print(report.to_summary())
        if args.output:
            with open(args.output, "w", encoding="utf-8") as f:
                f.write(report.to_markdown())
    elif args.cve:
        report = engine.analyze_cve(args.cve)
        print(report.to_summary())
        if args.output:
            with open(args.output, "w", encoding="utf-8") as f:
                f.write(report.to_markdown())
    elif args.file:
        with open(args.file, "r", encoding="utf-8") as f:
            text = f.read()
        report = engine.analyze_raw(text)
        print(report.to_summary())
        out_path = args.output or f"threat_intel_report_{datetime.now():%Y%m%d_%H%M%S}.md"
        with open(out_path, "w", encoding="utf-8") as f:
            f.write(report.to_markdown())
        print(f"[+] Report saved to {out_path}")
    else:
        panel = ThreatIntelPanel(engine)
        panel.run()
